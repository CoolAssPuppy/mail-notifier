enum OAuthSecret {
    private static let salt: [UInt8] = [
        0x84, 0xd7, 0x66, 0x87, 0x42, 0x55, 0x16, 0x90, 
        0x8f, 0xd2, 0xe9, 0x5b, 0x78, 0xe6, 0xbb, 0x68, 
        0x97, 0x95, 0x2b, 0xa6, 0x2c, 0x5a, 0x2d, 0x8d, 
        0x10, 0x96, 0xc4, 0x3c, 0xf6, 0xca, 0x37, 0x37, 
        0x1b, 0x90, 0xb4, 0xf4, 0xca, 0x55, 0x21, 0x21, 
        0x31, 0xc6, 0x0a, 0x2e, 0xbe, 0x8c, 0xb1, 0x88, 
        0x43, 0xa1, 0x4e, 0xbb, 0x8e, 0x3e, 0x66, 0xad, 
        0x98, 0xed, 0x40, 0x84, 0xa2, 0xe3, 0x38, 0xeb, 
    ]

    static var secret: String {
        let encoded: [UInt8] = [
            0xf0, 0xb2, 0x15, 0xf3, 0x31, 0x30, 0x75, 0xe2, 
            0xea, 0xa6, 
        ]

        return decode(encoded, cipher: salt)
    }

    static func decode(_ encoded: [UInt8], cipher: [UInt8]) -> String {
        String(decoding: encoded.enumerated().map { (offset, element) in
            element ^ cipher[offset % cipher.count]
        }, as: UTF8.self)
    }
}
