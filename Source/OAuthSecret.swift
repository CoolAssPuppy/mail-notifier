enum OAuthSecret {
    private static let salt: [UInt8] = [
        0xc9, 0x7d, 0xe6, 0x6e, 0x5f, 0x72, 0xaf, 0xa4, 
        0x7a, 0xfa, 0x25, 0xa5, 0x96, 0x3a, 0xd0, 0xba, 
        0xeb, 0x32, 0xc2, 0xf3, 0xc9, 0x4a, 0xe1, 0x51, 
        0x7a, 0xed, 0x88, 0xce, 0xa7, 0x29, 0x3b, 0x50, 
        0x5a, 0x0f, 0xdc, 0xcf, 0x6b, 0xfc, 0x15, 0xb8, 
        0xbf, 0x12, 0x97, 0x1a, 0xb7, 0x20, 0x58, 0xea, 
        0x4d, 0xc8, 0xd1, 0x7f, 0x93, 0xf6, 0xb8, 0xc7, 
        0x3c, 0x07, 0x73, 0xf3, 0x74, 0x07, 0x6a, 0x59, 
    ]

    static var secret: String {
        let encoded: [UInt8] = [
            0xbd, 0x18, 0x95, 0x1a, 0x2c, 0x17, 0xcc, 0xd6, 
            0x1f, 0x8e, 
        ]

        return decode(encoded, cipher: salt)
    }

    static func decode(_ encoded: [UInt8], cipher: [UInt8]) -> String {
        String(decoding: encoded.enumerated().map { (offset, element) in
            element ^ cipher[offset % cipher.count]
        }, as: UTF8.self)
    }
}
